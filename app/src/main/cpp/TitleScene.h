/******************************************************************************/
/*! @file       TitleScene.h
    @brief      タイトルシーンクラス定義ファイル
*******************************************************************************/

#ifndef TITLESCENE_H
#define TITLESCENE_H

#include "Scene.h"
#include "ButtonUI.h"

namespace Shooting2D
{
    /******************************************************************************/
    /*! @class CTitleScene
        @brief  タイトルシーンクラス
    *******************************************************************************/
    class CTitleScene : public IScene
    {
    private:

        /*! シーン変更フラグ */
        MyBool m_bSceneChange;

        /*!　BGM　*/
        MyInt m_BgmHandle;

        /*! 背景画像ID */
        MyInt m_Image;

        /*! ロゴ画像ID */
        MyInt m_LogoImage;

        /*! タイトルへ戻るボタン */
        ButtonPtr m_BtnTitle;

        /*! ボタンUI */
        CButtonUI m_BtnUI;

    public:
        /******************************************************************************/
        /*! コンストラクタ
        *******************************************************************************/
        CTitleScene();

        /******************************************************************************/
        /*! デストラクタ
        *******************************************************************************/
        virtual ~CTitleScene();

        /******************************************************************************/
        /*! 読み込み
            @return         成功 k_Success, 失敗 それ以外
        *******************************************************************************/
        virtual MyS32 Load() override;

        /******************************************************************************/
        /*! 初期化
            @return         成功 k_Success, 失敗 それ以外
        *******************************************************************************/
        virtual MyS32 Initialize() override;

        /******************************************************************************/
        /*! 更新
            @return         成功 k_Success, 失敗 それ以外
        *******************************************************************************/
        virtual MyS32 Update() override;

        /******************************************************************************/
        /*! 描画
            @return         成功 k_Success, 失敗 それ以外
        *******************************************************************************/
        virtual MyS32 Draw() override;

        /******************************************************************************/
        /*! 解放
            @return         成功 k_Success, 失敗 それ以外
        *******************************************************************************/
        virtual MyS32 Release() override;
    };

}


#endif //TITLESCENE_H
