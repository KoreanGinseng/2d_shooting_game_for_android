/******************************************************************************/
/*! @file       Player.h
    @brief      プレイヤー定義ファイル
*******************************************************************************/

#ifndef PLAYER_H
#define PLAYER_H

#include "GameObject.h"
#include "Turret.h"

namespace Shooting2D
{
    /******************************************************************************/
    /*! @class CPlayer
        @brief  プレイヤークラス
    *******************************************************************************/
    class CPlayer : public CGameObject
    {
    private:

        /*! 利用する画像ID */
        MyInt m_Image;
        MyInt m_TurretImage;

        /*! 弾発射用クラス */
        TurretPtr m_Turret[3];
        TurretPtr m_TurretCurrent;

    public:

        /******************************************************************************/
        /*! コンストラクタ
        *******************************************************************************/
        CPlayer();

        /******************************************************************************/
        /*! デストラクタ
        *******************************************************************************/
        virtual ~CPlayer() override;

        /******************************************************************************/
        /*! プレイヤーの読み込み
            @return         成功 k_Success, 失敗 それ以外
        *******************************************************************************/
        MyS32 Load();

        /******************************************************************************/
        /*! プレイヤーの初期化
            @return         成功 k_Success, 失敗 それ以外
        *******************************************************************************/
        MyS32 Initialize();

        /******************************************************************************/
        /*! プレイヤーの更新
            @return         成功 k_Success, 失敗 それ以外
        *******************************************************************************/
        virtual MyS32 Update() override;

        /******************************************************************************/
        /*! プレイヤーの描画
            @return         成功 k_Success, 失敗 それ以外
        *******************************************************************************/
        virtual MyS32 Draw() override;

        /******************************************************************************/
        /*! プレイヤーの解放
            @return         成功 k_Success, 失敗 それ以外
        *******************************************************************************/
        MyS32 Release();

        /******************************************************************************/
        /*! 砲台の変更
            @param[in]      no    変更する砲台番号
        *******************************************************************************/
        MyVoid ChangeTurret(MyS32 no);
    };

}

#endif //PLAYER_H
