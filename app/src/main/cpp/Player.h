/******************************************************************************/
/*! @file       Player.h
    @brief      プレイヤー定義ファイル
*******************************************************************************/

#ifndef PLAYER_H
#define PLAYER_H

#include "GameObject.h"

namespace Shooting2D
{
    /******************************************************************************/
    /*! @class CPlayer
        @brief  プレイヤークラス
    *******************************************************************************/
    class CPlayer : public CGameObject
    {
    private:

        /*! 利用する画像ID */
        MyInt m_Image;

    public:

        /******************************************************************************/
        /*! コンストラクタ
        *******************************************************************************/
        CPlayer();

        /******************************************************************************/
        /*! デストラクタ
        *******************************************************************************/
        virtual ~CPlayer() override;

        /******************************************************************************/
        /*! プレイヤーの読み込み
            @return         成功 k_Success, 失敗 それ以外
        *******************************************************************************/
        MyS32 Load();

        /******************************************************************************/
        /*! プレイヤーの初期化
            @return         成功 k_Success, 失敗 それ以外
        *******************************************************************************/
        MyS32 Initialize();

        /******************************************************************************/
        /*! プレイヤーの更新
            @return         成功 k_Success, 失敗 それ以外
        *******************************************************************************/
        virtual MyS32 Update() override;

        /******************************************************************************/
        /*! プレイヤーの描画
            @return         成功 k_Success, 失敗 それ以外
        *******************************************************************************/
        virtual MyS32 Draw() override;

        /******************************************************************************/
        /*! プレイヤーの解放
            @return         成功 k_Success, 失敗 それ以外
        *******************************************************************************/
        MyS32 Release();
    };

}

#endif //PLAYER_H
