/******************************************************************************/
/*! @file       ClearScene.h
    @brief      クリアシーンクラス定義ファイル
*******************************************************************************/

#ifndef CLEARSCENE_H
#define CLEARSCENE_H

#include "Scene.h"
#include "ButtonUI.h"

namespace Shooting2D
{
    /******************************************************************************/
    /*! @class CClearScene
        @brief  クリアシーンクラス
    *******************************************************************************/
    class CClearScene : public IScene
    {
    private:

        /*! シーン変更フラグ */
        MyBool m_bSceneChange;

        /*! 背景画像ID */
        MyInt m_Image;

        /*! ロゴ画像ID */
        MyInt m_LogoImage;

        /*! タイトルへ戻るボタン */
        ButtonPtr m_BtnTitle;

        /*! ボタンUI */
        CButtonUI m_BtnUI;
    public:

        /******************************************************************************/
        /*! コンストラクタ
        *******************************************************************************/
        CClearScene();

        /******************************************************************************/
        /*! デストラクタ
        *******************************************************************************/
        virtual ~CClearScene();

        /******************************************************************************/
        /*! 読み込み
            @return         成功 k_Success, 失敗 それ以外
        *******************************************************************************/
        virtual MyS32 Load() override;

        /******************************************************************************/
        /*! 初期化
            @return         成功 k_Success, 失敗 それ以外
        *******************************************************************************/
        virtual MyS32 Initialize() override;

        /******************************************************************************/
        /*! 更新
            @return         成功 k_Success, 失敗 それ以外
        *******************************************************************************/
        virtual MyS32 Update() override;

        /******************************************************************************/
        /*! 描画
            @return         成功 k_Success, 失敗 それ以外
        *******************************************************************************/
        virtual MyS32 Draw() override;

        /******************************************************************************/
        /*! 解放
            @return         成功 k_Success, 失敗 それ以外
        *******************************************************************************/
        virtual MyS32 Release() override;
    };

}


#endif //CLEARSCENE_H
