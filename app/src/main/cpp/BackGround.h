/******************************************************************************/
/*! @file       BackGround.h
    @brief      背景クラス定義ファイル
*******************************************************************************/

#ifndef BACKGROUND_H
#define BACKGROUND_H

#include "GameObject.h"

namespace Shooting2D
{
    /******************************************************************************/
    /*! @class CBackGround
        @brief  背景クラス
    *******************************************************************************/
    class CBackGround
    {
    private:
        /*! スクロール値 */
        MyFloat m_Scroll;

        /*! 背景の幅と高さ */
        MyS32   m_Width;
        MyS32   m_Height;

        /*! 利用する画像ID */
        MyS32   m_Image;

    public:

        /******************************************************************************/
        /*! コンストラクタ
        *******************************************************************************/
        CBackGround();

        /******************************************************************************/
        /*! デストラクタ
        *******************************************************************************/
        ~CBackGround();

        /******************************************************************************/
        /*! 背景クラスの読み込み
            @return         成功 k_Success, 失敗 それ以外
        *******************************************************************************/
        MyS32 Load();

        /******************************************************************************/
        /*! 背景クラスの初期化
            @return         成功 k_Success, 失敗 それ以外
        *******************************************************************************/
        MyS32 Initialize();

        /******************************************************************************/
        /*! 背景クラスの更新
            @return         成功 k_Success, 失敗 それ以外
        *******************************************************************************/
        MyS32 Update();

        /******************************************************************************/
        /*! 背景クラスの描画
            @return         成功 k_Success, 失敗 それ以外
        *******************************************************************************/
        MyS32 Draw();

        /******************************************************************************/
        /*! 背景クラスの解放
            @return         成功 k_Success, 失敗 それ以外
        *******************************************************************************/
        MyS32 Release();

        /******************************************************************************/
        /*! スクロール値の取得
            @return         スクロール値
        *******************************************************************************/
        MyFloat GetScroll() const noexcept
        {
            return m_Scroll;
        }
    };
}

#endif //BACKGROUND_H
